package Chap13_Array;

public class A02_ArrayIsInstance {
    public static void main(String[] args) {
        int[] ar = new int[50];
        System.out.println("length: " + ar.length);
        System.out.println(ar);

        ar = new int[100];
        System.out.println("length: " + ar.length);
        System.out.println(ar);
    }
}
